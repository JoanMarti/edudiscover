export function getCityBySlug(slug: string): City | undefined {
    return cities.find(city => city.slug === slug);
}

export function getTopCities(limit: number = 8): City[] {
    return cities
        .sort((a, b) => b.schoolCount - a.schoolCount)
        .slice(0, limit);
}

export function getCitiesByCommunity(community: string): City[] {
    return cities.filter(city => city.community === community);
}

export function getAllCommunities(): string[] {
    return Array.from(new Set(cities.map(city => city.community))).sort();
}
